import java.util.ArrayList;

public class OrderedArrayList{
  public ArrayList<Integer> oal;
  public OrderedArrayList(){
    oal = new ArrayList<Integer>();
  }
  
  public int size(){
    return oal.size();
  }
  public String toString(){
    return oal.toString();
  }
  public int get(int index){
    return oal(index);
  }
}
  
